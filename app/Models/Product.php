 * @method static \Illuminate\Database\Eloquent\Builder|Product whereUserId($value)
 * @mixin \Eloquent
 */
class Product extends Model
{
    use HasFactory;

    protected $fillable = [
        'name',
        'category',
        'price',
        'description',
        'href'
    ];

    public function user() {
        return $this->belongsTo(User::class, 'user_id', 'id');
    }
}
